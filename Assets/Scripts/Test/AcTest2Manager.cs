	// Use this for initialization
	void Start()
	{

		AcAd.onStart(this);
	}

	// Update is called once per frame
	void Update()
	{
		if ( Input.GetMouseButtonDown( 0 ) )
		{
			/*
			 * シーン名の取得
			 * http://blog.livedoor.jp/croakubi-dpro/archives/35068616.html
			 */
			var _sceneName = Application.loadedLevelName;

			switch(_sceneName)
			{
				case ( "Test1" ):
					Application.LoadLevel("Test2");
					break;
				case ( "Test2" ):
					Application.LoadLevel("Test1");
					break;
			}
		}
	}

	void OnGUI()
	{
		AcAd.onGui();
	}

